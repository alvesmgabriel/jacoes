    public void setDiretorio(String diretorio) {
        this.diretorio = diretorio;
        dlmAtivos.clear();
        incluirAtivos();
    }
    
    private String getSimbolo(String nome){
        // PETR4.csv => ["PETR4", "csv"]
        String [] tokens = nome.split("\\.");
        return tokens[0];
    }
    
    private void incluirAtivos() {
        File dir = new File(this.diretorio);
        File [] arquivos = dir.listFiles();
        for (File arquivo : arquivos) {
            dlmAtivos.addElement(getSimbolo(arquivo.getName()));
        }
    }
    
